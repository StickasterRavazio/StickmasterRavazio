-- ALgoritmo, o que seria? 
--Ele é uma sequência de comandos em uma ordem lógica e/ou sequencial também
--[[
    A Ordem lógica é definida na ação que vai ser realizada, exemplo: a conclusão da ação não se pode
vir primeiro que a própria inicialização, ou introdução, da ação. Um exemplo:
Na ação de jogar um papel no lixo o papel não pode estar no lixo antes de eu jogar o próprio papel.
Isso é lógico não é ?
isso é a ordem lógica
Agora a ordem seguencial é que a ação 1 vem antes da 2 e a 3 pós a 2, Exemplo:
Na ação de jogar o papel no lixo, em primeiro momento a pessoa tem que pegar o papel,
em segundo momento amassa-lo e por fim no terceiro momento localiazar a lixeira e jogar o papel.
A Ordem sequencial também lembra uma receita que fala passo a passo do que se fazer.
isso é a ordem sequencial.
O Algoritmo é a junção das duas
EXEMPLO:
ALGORITMO: COPIAR UM TEXTO NO PAPEL
(1) Localizar o papel, o texto e o lápis
(2) Copiar texto no papel
Agora caso mudarmos a sequência perderia a lógica
(1) Copiar texto no papel
(2) Localizar o papel, o texto e o lápis
Isso é um algoritmo, algo sequencial e lógico
Então pra resumir TUDO isso o algoritmo é a execução de uma tarefa em uma ordem lógia e sequencial
com o intenção de cumprir um objetivo pré-definido.
O RESUMO DO RESUMO: é um tanto de tarefa pra terminar algo
]]--