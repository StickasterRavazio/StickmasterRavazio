-- Os escapes de texto é o método de se usar simbolos do código funcionarem como string
--Exemplo:
var = 'o cara é tão \'rico\' quanto o merely no roblox'
print(var)
-- para utilizar o escape eu usei a \ antes do simbolo de aspas para representar ironia
-- Agora, caso eu queira imprimir um texto com várias linhas como eu faria ?
--Exemplo
var = 'um texto de cria com varias linhas como essa: \n ta vendo ?'
print(var)
--\n
--obs
var = 'caso queira printar a barra utilize o escape duas vezes \\ \n né segredo'
