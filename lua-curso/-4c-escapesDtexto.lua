-- para adcionar um parágrafo na string utilize-se \t pra não precisar usar vários espaços e deixar o código bagunced
var = '\t era uma vez... um cria no topo do morro...'
print(var)
--\t
var = '\t\rTomelé'
print(var)
-- nesse eu usei \r que é o return, pra desfazer mudanças na string, que era o parágrafo \t.


